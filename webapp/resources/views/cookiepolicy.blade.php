<div class="h2">
Che cosa è questa Politica sui cookie?
</div>

Questa Politica sui cookie espone come noi, e i terzi da noi autorizzati, utilizziamo i cookie quando voi interagite con il nostro Sito o i servizi offerti tramite il Sito. Vogliate tenere presente che questa Politica sui cookie è parte della nostra Informativa sulla Privacy e deve essere letta in aggiunta ad essa. La nostra Informativa sulla Privacy spiega cosa facciamo (e cosa non facciamo) con le informazioni, compresi tutti i dati personali che potremmo raccogliere sul vostro conto. Siete pregati di leggere entrambe.

<div class="h2">
Che cosa sono i cookie?
</div>

I cookie sono identificatori univoci, generalmente composti di piccole stringhe di testo o codice. Di regola, i cookie sono memorizzati sul vostro dispositivo o nel vostro browser e inviano determinate informazioni alla parte che ha trasmesso il cookie. Quando parliamo di cookie, ci riferiamo in realtà a una più vasta gamma di tecnologie piuttosto che soltanto ai cookie, compresi web beacon, clear gifs, pixel e altre tecnologie simili. Quando parliamo di cookie proprietari, intendiamo i cookie trasmessi da Buyersharing. Quando parliamo di cookie di terzi, intendiamo i cookie trasmessi da soggetti terzi estranei a Buyersharing.


<div class="h2">
Come sono utilizzati i cookie?
</div>

I cookie sono utilizzati per comprendere meglio come lavorano le applicazioni e i siti web e per ottimizzare le esperienze online e mobili. I cookie fanno ciò consentendo alla parte che trasmette il cookie di analizzare le prestazioni di un sito web o di un'app mobile, di identificare determinati utenti in modo anonimo e ricordare le loro preferenze, di comprendere meglio se un computer o dispositivo (e probabilmente il suo utente) ha già visitato un sito web e di fornire pubblicità personalizzate.


<div class="h2">
Che tipi di cookie sono utilizzati nel Sito?
</div>

Il nostro Sito utilizza principalmente i seguenti quattro tipi di cookie:

<div class="h3">
1. Cookie operativamente necessari
</div>


Questi cookie sono essenziali per l’operatività del nostro sito. Essi vi consentono di navigare e di utilizzare il nostro sito. Se disattivate questi cookie, il sito non funzionerà correttamente.

<div class="h3">
2. Cookie sulle prestazioni
</div>


Questi cookie raccolgono informazioni su come i visitatori utilizzano il Sito e monitorano le prestazioni del sito. Ad esempio, i cookie sulle prestazioni ci dicono quali pagine sono popolari, monitorano il traffico verso il nostro Sito e compilano informazioni analitiche in forma anonima. I cookie sulle prestazioni possono essere utilizzati anche per identificare e correggere problemi operativi con il Sito.

<div class="h3">
3. Cookie funzionali
</div>


Questi cookie sono impiegati per riconoscere e ricordare le vostre preferenze. Ad esempio, i cookie funzionali potrebbero utilizzare informazioni, come la vostra ubicazione, per garantire che accediate a una versione del nostro sito specifica per la vostra città o regione.

<div class="h3">
4. Cookie pubblicitari
</div>


Autorizziamo terzi a fornire contenuti pubblicitari personalizzati sul nostro sito e/o su siti web e applicazioni di terzi. Tali terzi utilizzano cookie per comprendere il vostro interesse riguardo alle pubblicità e ai contenuti a voi forniti, nonché per offrirvi pubblicità più mirate sulla base delle vostre attività sul nostro Sito. Ad esempio, i cookie di terzi potrebbero registrare informazioni sul fatto che abbiate cliccato su determinati contenuti o determinate pubblicità sul nostro Sito o su altri siti web, o inserito un articolo sul nostro sito nel vostro carrello acquisti.

Non abbiamo controllo su o accesso ai cookie pubblicitari di terzi o alle informazioni in essi contenute. I terzi gestiscono tutte le informazioni che raccolgono tramite i cookie da loro piazzati in conformità alle loro politiche sulla privacy.

Potete disattivare i cookie pubblicitari personalizzati di terzi (tenete però presente che continuerete a ricevere pubblicità online). Per ulteriori informazioni o per disattivare l'utilizzo di tali cookie pubblicitari personalizzati di terzi nell'UE, visitate www.youronlinechoices.eu Se siete residenti negli Stati Uniti, visitate http://www.aboutads.info/choices/.

